function findVeh(id)
	local vehicles = getElementsByType("vehicle")
	for k,v in pairs(vehicles) do
		if getElementData(v,"id") == id then
			return v
		end
	end
	return false
end

function countvehicleinserver(id)
    local vehicles = getElementsByType("vehicle")
    local i=0 
    for k,v in pairs(vehicles) do
        if getElementData(v,"Model") == tonumber(id) then
            i=i+1
		end
    end
    if i <= 0 then
        return 0
    elseif i > 0 then
        return i
    end
end

function convertNumber(amount)
	local formatted = amount
	while true do  
        formatted, k = string.gsub(formatted, "^(-?%d+)(%d%d%d)", '%1 %2')
        if (k==0) then
            break
        end
  	end
	return formatted
end

